public class typeCasting {

    public static void main(String[] args) {

        // implicit type casting
         
        int i = 10;

        long l = i;

        double d = l;

        System.out.println(i);
        System.out.println(l);
        System.out.println(d);


        //explicit type casting

        double var1 = 10;


        int var2 = (int)var1;

        System.out.println(var2);

       
    }
    
}
